.MODEL SMALL 
.STACK 100H
.DATA 
MSG1 DB 'ENTER MESSAGE TO DISPLAY:$'
MSG2 DB 13,10, 'YOU ENTERED : $'

.CODE 
MAIN PROC 
  MOV AX, @DATA 
  MOV DS, AX 

  LEA DX,MSG1 
  MOV AH, 09H 
  INT 21H 

  MOV AH, 01H 
  INT 21H 
  MOV BL, AL 

  LEA DX, MSG2 
  MOV AH, 09H 
  INT 21H 

  MOV DL, BL
  MOV AH , 02H 
  INT 21H 

  MOV AH, 4CH 
  INT 21H 
  MAIN ENDP
END MAIN

  