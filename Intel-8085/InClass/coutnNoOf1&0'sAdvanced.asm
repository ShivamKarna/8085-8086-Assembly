; TO COUNT NO OF 1'S AND 0'S IN 20 BYTES OF DATA

MVI A, 02H ; HOW MANY NUMBERS OF DATA TO PUT
LXI H, 5000H ; TO STORE THE NUMBER
LXI B, 6000H ; TO STORE 0'S
LXI D, 7000H ; TO STORE 1'S

REPEAT: CALL NEXT
JNZ REPEAT
HLT

NEXT: PUSH PSW
PUSH H
MOV A, M
MVI H, 08H ; 8 BIT OF 0'S AND 1'S
MVI L, 00H ; COUNT 0 ONLY

BACK: RRC
JC ONE
INR L 

ONE: DCR H
JNZ BACK 
MOV A, L
STAX B ; TOTAL NO. OF COUNTED ZERO
MVI A, 08H
SUB L 
STAX D ; TOTAL NO. OF COUNTED ONE
INX B
INX D
POP H
POP PSW
DCR A 
INX H
RET

